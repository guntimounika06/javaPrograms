package JAVAPROGRAMS;
public class EnhancedForLoop
{
    public static void main(String[] args)
    {
        int[] nums = new int[4];

       nums[0] = 4;
       nums[1] = 5;
       nums[2] = 6;
       nums[3] = 7;

       for(int a : nums)
       {
           System.out.println(a);

       }

    }
}
